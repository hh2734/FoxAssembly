		local temp = {}
		for j = 1, 4 do
			temp[j] = key_schedule[i-1][j]
		end
		
		if i % nk == 0 then
			temp = sub_word(rot_word(temp))
			temp[1] = temp[1] ~ Rcon[math.floor(i / nk)]
		elseif nk > 6 and i % nk == 4 then
