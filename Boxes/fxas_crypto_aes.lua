local function key_expansion(key)
	local key_schedule = {}
	local nk = 8
	local nb = 4
	local nr = 14
	for i = 1, nk do
		key_schedule[i] = {
			key[(i-1)*4 + 1] or 0,
			key[(i-1)*4 + 2] or 0,
			key[(i-1)*4 + 3] or 0,
			key[(i-1)*4 + 4] or 0
		}
	end
